#!/usr/bin/env node
/**
 * Emit Rust constants and proof bundles for groth16-solana-compatible
 * Anchor verifier programs.
 *
 * Three modes:
 *   --kind vkey       Print a `src/vkey.rs` (alpha_g1 / beta_g2 / gamma_g2 /
 *                     delta_g2 / IC + `pub const VKEY: Groth16Vkey = ...`).
 *
 *   --kind fixture    Run `snarkjs.groth16.fullProve` for the given inputs and
 *                     print two consts (TEST_PROOF: [u8;256] and
 *                     TEST_PUBLIC_INPUTS: [[u8;32]; N]) suitable for pasting
 *                     into a Rust integration test.
 *
 *   --kind bundle     Run `snarkjs.groth16.fullProve` for the given inputs and
 *                     print a JSON `{ "proofHex": "...", "publicSignalsHex":
 *                     [...] }` bundle, in the schema consumed by the
 *                     provider-sdk `submit-vault-proof` CLI.
 *
 * Byte transformations (BN254, big-endian, c1-first G2; matches the
 * convention proven correct in `vault-verifier-core`):
 *   - Field element  → 32 BE bytes (BigInt.toString(16), padStart(64)).
 *   - G1 point       → `BE(x) || BE(y)` (64 B).
 *   - G2 point       → `BE(x_c1) || BE(x_c0) || BE(y_c1) || BE(y_c0)` (128 B).
 *   - `proof.pi_a.y` must be negated: `(p - y) mod p`, where p is the BN254
 *     base-field modulus. Matches snarkjs `exportSolidityCallData`.
 *
 * Usage:
 *   node scripts/emit-rust-vkey.mjs --circuit aggregate_sum --kind vkey \
 *     > ../anchor-program/programs/vault_verifier_aggregate_sum/src/vkey.rs
 *
 *   node scripts/emit-rust-vkey.mjs --circuit aggregate_sum --kind fixture \
 *     --inputs '{"values":[1,2,3,4,5,6,7,8],"claimedSum":36}' \
 *     > /tmp/aggregate_sum_fixture.rs
 *
 *   node scripts/emit-rust-vkey.mjs --circuit aggregate_sum --kind bundle \
 *     --inputs '{"values":[1,2,3,4,5,6,7,8],"claimedSum":36}' \
 *     > /tmp/aggregate_sum_bundle.json
 */

import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { readFileSync, existsSync } from 'node:fs';
import { groth16 } from 'snarkjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, '..');
const buildDir = path.join(root, 'build');

// BN254 base-field modulus (alt_bn128 / bn254). Same value used in
// groth16-solana's parse_vk_to_rust.js for the pi_a.y negation.
const P = 21888242871839275222246405745257275088696311157297823662689037894645226208583n;

function parseArgs(argv) {
  const out = {};
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a.startsWith('--')) {
      const k = a.slice(2);
      const v = argv[i + 1];
      out[k] = v;
      i++;
    }
  }
  return out;
}

function toBeBytes32(decimalStr) {
  const hex = BigInt(decimalStr).toString(16).padStart(64, '0');
  const out = new Uint8Array(32);
  for (let i = 0; i < 32; i++) {
    out[i] = parseInt(hex.slice(i * 2, i * 2 + 2), 16);
  }
  return out;
}

/** Encode a G1 point `[x, y, 1]` as BE(x) || BE(y) (64 bytes). */
function encodeG1(point) {
  const x = toBeBytes32(point[0]);
  const y = toBeBytes32(point[1]);
  const out = new Uint8Array(64);
  out.set(x, 0);
  out.set(y, 32);
  return out;
}

/**
 * Encode a G2 point `[[x0, x1], [y0, y1], [1, 0]]` as
 * BE(x_c1) || BE(x_c0) || BE(y_c1) || BE(y_c0) (128 bytes).
 * The `c1-first` ordering is required by Solana's alt_bn128 syscalls.
 */
function encodeG2(point) {
  const xC0 = toBeBytes32(point[0][0]);
  const xC1 = toBeBytes32(point[0][1]);
  const yC0 = toBeBytes32(point[1][0]);
  const yC1 = toBeBytes32(point[1][1]);
  const out = new Uint8Array(128);
  out.set(xC1, 0);
  out.set(xC0, 32);
  out.set(yC1, 64);
  out.set(yC0, 96);
  return out;
}

/** Negate the y-coordinate of a G1 point: (p - y) mod p. */
function negateG1Y(point) {
  const x = point[0];
  const y = (P - (BigInt(point[1]) % P)) % P;
  return [x, y.toString(), point[2]];
}

/** Format a Uint8Array as a Rust array body, wrapping every 16 bytes. */
function formatBytes(bytes, indent = '    ') {
  const lines = [];
  for (let i = 0; i < bytes.length; i += 16) {
    const slice = Array.from(bytes.slice(i, i + 16)).join(', ');
    lines.push(indent + slice + ',');
  }
  return lines.join('\n');
}

function emitVkey(circuit) {
  const vkeyPath = path.join(buildDir, `${circuit}_vkey.json`);
  if (!existsSync(vkeyPath)) {
    throw new Error(`Missing ${vkeyPath}. Run setup-dev.mjs first.`);
  }
  const vkey = JSON.parse(readFileSync(vkeyPath, 'utf-8'));

  const alphaG1 = encodeG1(vkey.vk_alpha_1);
  const betaG2 = encodeG2(vkey.vk_beta_2);
  const gammaG2 = encodeG2(vkey.vk_gamma_2);
  const deltaG2 = encodeG2(vkey.vk_delta_2);
  const ic = vkey.IC.map((pt) => encodeG1(pt));

  const icBlocks = ic
    .map(
      (bytes) =>
        `    [\n${formatBytes(bytes, '        ')}\n    ],`,
    )
    .join('\n');

  const header = [
    `//! Auto-generated by packages/vault-circuits/scripts/emit-rust-vkey.mjs.`,
    `//! Circuit: ${circuit}`,
    `//! Source: packages/vault-circuits/build/${circuit}_vkey.json`,
    `//! DEV VKEY — DO NOT DEPLOY TO MAINNET. Regenerate after trusted-setup ceremony.`,
    ``,
    `use vault_verifier_core::Groth16Vkey;`,
    ``,
    `const IC: &[[u8; 64]] = &[`,
    icBlocks,
    `];`,
    ``,
    `pub const VKEY: Groth16Vkey = Groth16Vkey {`,
    `    alpha_g1: [`,
    formatBytes(alphaG1, '        '),
    `    ],`,
    `    beta_g2: [`,
    formatBytes(betaG2, '        '),
    `    ],`,
    `    gamma_g2: [`,
    formatBytes(gammaG2, '        '),
    `    ],`,
    `    delta_g2: [`,
    formatBytes(deltaG2, '        '),
    `    ],`,
    `    ic: IC,`,
    `};`,
    ``,
  ];
  return header.join('\n');
}

/**
 * Run the prover and return Solana-encoded bytes:
 *   - `proofBytes`: 256-byte Uint8Array (a || b || c, with pi_a.y negated).
 *   - `signalBytes`: Uint8Array[] of 32-byte BE-encoded public signals.
 *   - `publicSignals`: raw decimal strings from snarkjs, retained for
 *     audit / fixture annotations.
 *
 * Exported so vitest (and any future glue script) can reach for the same
 * byte computation that backs `--kind fixture` and `--kind bundle`
 * without spawning a child process.
 */
export async function proveAndEncode(circuit, inputs) {
  const wasm = path.join(buildDir, `${circuit}_js`, `${circuit}.wasm`);
  const zkey = path.join(buildDir, `${circuit}_final.zkey`);
  for (const p of [wasm, zkey]) {
    if (!existsSync(p)) throw new Error(`Missing artifact: ${p}`);
  }

  const { proof, publicSignals } = await groth16.fullProve(inputs, wasm, zkey);

  // proof.pi_a.y must be negated for Solana verification (matches snarkjs
  // exportSolidityCallData).
  const a = encodeG1(negateG1Y(proof.pi_a));
  const b = encodeG2(proof.pi_b);
  const c = encodeG1(proof.pi_c);

  const proofBytes = new Uint8Array(256);
  proofBytes.set(a, 0);
  proofBytes.set(b, 64);
  proofBytes.set(c, 192);

  const signalBytes = publicSignals.map((s) => toBeBytes32(s));

  return { proofBytes, signalBytes, publicSignals };
}

async function emitFixture(circuit, inputs) {
  const { proofBytes, signalBytes, publicSignals } = await proveAndEncode(circuit, inputs);
  const n = signalBytes.length;
  const signalsRust = signalBytes
    .map((bytes) => `    [\n${formatBytes(bytes, '        ')}\n    ],`)
    .join('\n');

  const header = [
    `// Auto-generated by packages/vault-circuits/scripts/emit-rust-vkey.mjs.`,
    `// Circuit: ${circuit}`,
    `// Inputs: ${JSON.stringify(inputs)}`,
    `// publicSignals: ${JSON.stringify(publicSignals)}`,
    ``,
    `const TEST_PROOF: [u8; 256] = [`,
    formatBytes(proofBytes, '    '),
    `];`,
    ``,
    `const TEST_PUBLIC_INPUTS: [[u8; 32]; ${n}] = [`,
    signalsRust,
    `];`,
    ``,
  ];
  return header.join('\n');
}

/**
 * Run the prover and return the JSON proof-bundle string consumed by
 * the provider-sdk `submit-vault-proof` CLI:
 *
 * ```json
 * {
 *   "proofHex": "<512 hex chars = 256 bytes>",
 *   "publicSignalsHex": ["<64 hex chars = 32 bytes>", ...]
 * }
 * ```
 *
 * No `0x` prefix on the hex fields; the CLI loader accepts either form.
 * The bundle is exactly the same byte stream the `--kind fixture` mode
 * embeds in Rust constants, so a single proof can be checked both
 * on-chain (via this bundle) and against a Rust integration test (via
 * the fixture).
 */
export async function buildBundle(circuit, inputs) {
  const { proofBytes, signalBytes } = await proveAndEncode(circuit, inputs);
  const bundle = {
    proofHex: bytesToHex(proofBytes),
    publicSignalsHex: signalBytes.map((b) => bytesToHex(b)),
  };
  return JSON.stringify(bundle, null, 2) + '\n';
}

function bytesToHex(bytes) {
  let out = '';
  for (const b of bytes) {
    out += b.toString(16).padStart(2, '0');
  }
  return out;
}

async function main() {
  const args = parseArgs(process.argv);
  if (!args.circuit) {
    console.error('usage: --circuit <name> --kind vkey|fixture|bundle [--inputs JSON]');
    process.exit(1);
  }
  const kind = args.kind || 'vkey';

  if (kind === 'vkey') {
    process.stdout.write(emitVkey(args.circuit));
  } else if (kind === 'fixture') {
    if (!args.inputs) {
      console.error('--kind fixture requires --inputs JSON');
      process.exit(1);
    }
    const parsed = JSON.parse(args.inputs);
    const out = await emitFixture(args.circuit, parsed);
    process.stdout.write(out);
  } else if (kind === 'bundle') {
    if (!args.inputs) {
      console.error('--kind bundle requires --inputs JSON');
      process.exit(1);
    }
    const parsed = JSON.parse(args.inputs);
    const out = await buildBundle(args.circuit, parsed);
    process.stdout.write(out);
  } else {
    console.error(`unknown --kind: ${kind}`);
    process.exit(1);
  }
}

// Only run the CLI driver when invoked directly. Importing the module
// (e.g. from vitest) should not auto-execute main().
//
// snarkjs leaves worker handles alive after fullProve resolves which
// prevents Node from exiting on its own, so the CLI explicitly exits
// once main() resolves. (Library callers can simply await the named
// exports and let their host decide.)
if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  main()
    .then(() => process.exit(0))
    .catch((e) => {
      console.error(e);
      process.exit(1);
    });
}
